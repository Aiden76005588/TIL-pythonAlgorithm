# 최적화 문제란 어떤 함수의 값을 가능한 낮추거나 혹은 어떠한 함수의 값을 최대한 높이거나 하는 등의 문제
# 이런 최적환 문제를 바로 해결하기 어려운 경우 그 문제를 여러번의 결정 문제를 이용해서 문제형태를 바꾸어서 해결할 수 있는 경우가 존재
# 예시 : 특정한 조건을 만족하는 가장 알맞은 값을 빠르게 찾는 최적화 문제
